    createBlocks(blockSize = 12){
        let width = blockSize;
        let height = blockSize;
        for(let row=0; row < this.mapaInterior.length; row++){
            
            for(let column=0; column < this.mapaInterior[row].length; column++){
            
                let x = blockSize*(this.mapaInterior[row][column] * column);
                let y = blockSize*(this.mapaInterior[row][column] * row);
                
                
                if(this.mapaInterior[row][column] > 0){
                    this.mapElements.blocos.push(new bloco(x, 
                        y, 
                        width, 
                        height));
                }else{//Gerando os blocos com item.
                    if (row > 1 && column > 1 && row < this.mapaInterior.length-2 && column < this.mapaInterior[row].length-2){
                        const randon = Math.floor(Math.random()*400);
                        if (randon === 1) {
                            x = blockSize*(1 * column);
                            y = blockSize*(1 * row);
                            this.mapElements.items.push(new bloco(x, 
                                                        y, 
                                                        width, 
                                                        height,
                                                        true));
                        }
                    }
                }
            }
        }

        return this.mapElements;
    },

    renderMap(ctx, color){
        for (const b of this.mapElements.blocos) {
            ctx.fillStyle= color;
            ctx.fillRect(b.x, b.y, b.width, b.height);
        }

        for (const b of this.mapElements.items) {
            ctx.fillStyle= '#a00';
            ctx.fillRect(b.x, b.y, b.width, b.height);
        }
    }

